/*kelvin
project from Codecademy/JavaScript: changing Kelvin Weather to fahrenheit*/
var kelvin = 293;
var celsius = kelvin - 273;
var fahrenheit = celsius * (9/5) + 32;
console.log('The temperature is ' + fahrenheit + ' fahrenheit.');
/*dogyears
using JavaScript to convert my age into dog years*/
var myAge = 20;
var firstYears = 2 * 10.5;
var laterYears = (myAge - 2) * 4;
var myAgeInDogYears = firstYears + laterYears;
console.log('My age in dog years is ' +myAgeInDogYears + '.');
